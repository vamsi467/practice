son.map((item, index) => {
  const sources = item.config.map(({ datasource }) => datasource);
  const uniqueSources = [...new Set(sources)]
  const name =
    (item.settings && item.settings.chartTitle
      ? item.settings.chartTitle
          .trim()
          .toLowerCase()
          .replace(/ /g, "_")
          .replace(/-/g, "_")
          .replace(/__/g, "_")
      : index) +
    "_" +
    uniqueSources.join().toLowerCase().replace(/,/g, "_");

  const i = uniqueMetrics.findIndex(({ uniqueName }) => uniqueName === name);
  if (i > -1) {
    uniqueMetrics[i].categories.push(...item.categories);
  } else {
    item.uniqueName = name;
    uniqueMetrics.push(item);
  }

  delete item.drilldown;
});
uniqueMetrics.map((item, index) => {
  fs.writeFile(`${item.uniqueName}.json`, JSON.stringify(item), () => {
    console.log(index, "done");
  });
});
